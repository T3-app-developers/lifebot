let cachedArrayBuffer = null;

function decodeBase64ToArrayBuffer(base64String) {
  if (typeof atob === 'function') {
    const binaryString = atob(base64String);
    const length = binaryString.length;
    const bytes = new Uint8Array(length);
    for (let i = 0; i < length; i += 1) {
      bytes[i] = binaryString.charCodeAt(i);
    }
    return bytes.buffer;
  }
  if (typeof Buffer !== 'undefined') {
    const nodeBuffer = Buffer.from(base64String, 'base64');
    return nodeBuffer.buffer.slice(
      nodeBuffer.byteOffset,
      nodeBuffer.byteOffset + nodeBuffer.byteLength
    );
  }
  throw new Error('No base64 decoder available in this environment.');
}

export function getDashGlbArrayBuffer() {
  if (!cachedArrayBuffer) {
    cachedArrayBuffer = decodeBase64ToArrayBuffer(dashGlbBase64);
  }
  return cachedArrayBuffer.slice(0);
}
